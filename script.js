    function add(){
        var x = parseFloat(document.getElementById('x').value);
        var y = parseFloat(document.getElementById('y').value);
        document.getElementById('result').value = x+y;
    }
    function sub(){
        var x = parseFloat(document.getElementById('x').value);
        var y = parseFloat(document.getElementById('y').value);
        document.getElementById('result').value = x-y;
    }
    function mul(){
        var x = parseFloat(document.getElementById('x').value);
        var y = parseFloat(document.getElementById('y').value);
        document.getElementById('result').value = x*y;
    }
    function divi(){
        var x = parseFloat(document.getElementById('x').value);
        var y = parseFloat(document.getElementById('y').value);
        document.getElementById('result').value = x/y;
    }
